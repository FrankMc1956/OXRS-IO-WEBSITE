<h3 id="whats-the-aim-of-oxrs" tabindex="-1"><a class="header-anchor" href="#whats-the-aim-of-oxrs" aria-hidden="true">#</a> Whats the aim of OXRS?</h3>
<p>It aims to make it easier for anyone to build their own rack-mount devices by providing these foundational pieces in a modular form.</p>
<p><img src="/images/OXRS-prototype-1.jpg" alt="OXRS-prototype"></p>
<p>This convention grew out of DIY home automation systems developed by members of the SuperHouse Automation Discord server, but also has applications for industrial control, networking, audio, video, and data center equipment.</p>
<p>Most rack-mount projects have some common elements, including:</p>
<ul>
<li>Chassis</li>
<li>Display</li>
<li>Ethernet</li>
<li>USB</li>
<li>Power indicator</li>
</ul>
<p>When individuals undertake DIY rack-mount projects it’s necessary to re-invent these foundational elements before it’s possible to create the specific functionality required for the intended project.</p>
<h3 id="mechanical-and-electrical-compatibility" tabindex="-1"><a class="header-anchor" href="#mechanical-and-electrical-compatibility" aria-hidden="true">#</a> Mechanical and Electrical Compatibility</h3>
<p>The convention outlines mechanical dimensions for enclosures including front panel cutouts and internal PCB mounts, allowing individual Makers and companies to produce components that are mechanically and electrically compatible.</p>
