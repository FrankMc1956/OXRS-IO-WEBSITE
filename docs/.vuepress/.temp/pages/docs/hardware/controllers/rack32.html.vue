<p>A general purpose control board for rack mount projects.</p>
<p>DIY rack-mount projects often require an Ethernet connection, a status display, power regulation, and other features that are not specific to the project.</p>
<p>This board fits into a rack-mount case and provides those common features so they don't have to be recreated every time.</p>
<h2 id="features" tabindex="-1"><a class="header-anchor" href="#features" aria-hidden="true">#</a> Features</h2>
<ul>
<li><strong>ESP32</strong> microcontroller with <strong>WiFi</strong>.</li>
<li><strong>10/100Mbps</strong> Ethernet.</li>
<li>Support for both DIY (12V) PoE and <strong>802.3af PoE</strong>.</li>
<li>Pluggable terminal block for optional external DC power input.</li>
<li>Automotive-grade voltage regulator for harsh electrical environments.</li>
<li>6-way IDC &quot;I2C-Breakout&quot; header for linking to other boards.</li>
<li>8-way IDC &quot;SPI-LCD&quot; header for connection to an LCD.</li>
<li><strong>USB-C</strong> connector for loading firmware.</li>
<li>Auto power source selection between USB-C, PoE, and external DC.</li>
<li>I/O header with power, 2 x I2C ports, and second hardware serial port.</li>
<li>Micro SD card slot for config files, logs, and media storage.</li>
<li>Onboard <strong>temperature sensor</strong>.</li>
<li>Header for front-panel power LED.</li>
<li>Can be built with onboard PCB antenna or external WiFi antenna.</li>
</ul>
